package spring.core.exception;

public class Exception3 extends Exception {

	private static final long serialVersionUID = 1L;
	
	public Exception3()
	{
		super();
	}
	
	public Exception3(String message)
	{
		super(message);
	}
	
	public Exception3(String message, Throwable cause)
	{
		super(message, cause);
	}

}
